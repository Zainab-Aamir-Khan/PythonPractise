#sets in python
mySets = {21, "zainab", "safa", True, False, 12.3}
print(mySets)
print(type(mySets))
print(len(mySets))

#set constructor
newSet = set(("sana", "safa", "zainab"))
print(newSet)
print(type(newSet))

#set methods
print(len(newSet))
print(newSet.remove("sana"))
print(newSet.clear())


#pop method
a = {"Sana", "safa", "zainab"}
a.pop()
print(a)




